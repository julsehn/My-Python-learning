import math

num = 25
sqrt_num = math.sqrt(num)
print(f"L'arrel quadrada de {num} és {sqrt_num}")

angle = 45
cos_angle = math.cos(math.radians(angle))
print(f"El cosinus de {angle} graus és {cos_angle}")

angle = 60
sin_angle = math.sin(math.radians(angle))
print(f"El sinus de {angle} graus és {sin_angle}")

num = 10
ln_num = math.log(num)
print(f"El logaritme natural de {num} és {ln_num}")

num1 = 24
num2 = 36
mcd = math.gcd(num1, num2)
print(f"El màxim comú divisor de {num1} i {num2} és {mcd}")